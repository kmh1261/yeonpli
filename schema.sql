  `record_metric_value1` VARCHAR(255),
  `record_metric_title2` VARCHAR(100),
  `record_metric_value2` VARCHAR(255)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;

